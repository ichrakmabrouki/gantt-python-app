def parse_dat_to_dict(params, nbtechs, cte, gammes_raw, 
                      modes_raw, pt_raw, grp_mchs_raw):
    """
    Convertit les données du fichier .dat en dict Python.
    """
    return {
        'params': {
            'nbJobs': params[0],
            'nbMchs': params[1], 
            'nbOps':  params[2]
        },
        'nbtechs': nbtechs,
        'cte':     cte,
        'gammes':  gammes_raw,   # [(op_id, job_id, pos), ...]
        'modes':   modes_raw,    # [(op_id, mch), ...]
        'pt':      pt_raw,       # {(op_id, mch): duration}
        'grp_mchs': grp_mchs_raw # [(tech_id, mch), ...]
    }

